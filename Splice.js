// Understanding .splice(index, n)

// Splice: to remove element from the between. Example:
var arr = [1, 2, 3, 4, 5, 6, 7]
// to remove 3, count index of 3 first and keep it in splice then count how many elements to remove, if 1 element then write .splice(index, 1) if 2 element then write .splice(index, 2)
arr.splice(2, 1); //3 will be removed from the array
