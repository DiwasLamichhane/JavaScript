// Understanding Objects in JS

//Objects are used to store details of an item. Example:
let fruit = {
    name: "Apple",
    place: "Mustang",
    price: "RS180 per KG",
    taste: "Sweet"
}
// this is an object
// Now, to access we write,
// objectname.property EXAMPLE:

fruit.taste //'sweet' will be printed

//we can also change the value, EXAMPLE:
fruit.name = "Orange";
// name will be changed into Orange now. 
