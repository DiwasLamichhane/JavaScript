// Understanding Truthy and Falsy

/*

whatever we write in javascript, it belongs to either Truthy or Falsy

Falsy Values are: 0, false, undefined, null, NaN, document.all

Except above values, all others are truthy values

*/

// Example of Truthy
if(69){
    console.log("Since it is truthy, this will print!");
}
else{
    console.log("Since it is falsy, this will run!")
}
